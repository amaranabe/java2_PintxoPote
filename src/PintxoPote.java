class PintxoPote {

	public PintxoPote() {

	}

	private Pintxo pintxo;
	private Pote pote;
	private double precio;

	public Pintxo getPintxo() {
		return pintxo;		
	}

	public void setPintxo(Pintxo idpintxo) {
		this.pintxo=idpintxo;
	}

	public Pote getPote() {
		return pote;
	}

	public void setPote(Pote idpote) {
		this.pote=idpote;
	}

	public double getPrecio() {
		return precio;
	}

	public void setPrecio(double idprecio) {
		this.precio=idprecio;
	}

}